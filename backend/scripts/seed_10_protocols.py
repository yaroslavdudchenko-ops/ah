# ── Главная функция ────────────────────────────────────────────────────────────

async def main():
    engine = create_async_engine(settings.DATABASE_URL, echo=False)
    Session = async_sessionmaker(engine, expire_on_commit=False)

    async with Session() as db:
        # Проверяем, каких препаратов ещё нет в базе
        result = await db.execute(select(Protocol.drug_name))
        existing_drugs = {row[0] for row in result.all()}
        print(f"\nУже в базе: {sorted(existing_drugs)}\n")

        added = 0
        skipped = 0

        for proto in PROTOCOLS:
            if proto["drug_name"] in existing_drugs:
                print(f"  [skip] {proto['drug_name']} — уже существует")
                skipped += 1
                continue

            pid = _uuid()
            now = _now()

            p = Protocol(
                id=pid,
                title=proto["title"],
                drug_name=proto["drug_name"],
                inn=proto["inn"],
                phase=proto["phase"],
                therapeutic_area=proto["therapeutic_area"],
                indication=proto["indication"],
                population=proto["population"],
                primary_endpoint=proto["primary_endpoint"],
                secondary_endpoints=proto["secondary_endpoints"],
                duration_weeks=proto["duration_weeks"],
                dosing=proto["dosing"],
                inclusion_criteria=proto["inclusion_criteria"],
                exclusion_criteria=proto["exclusion_criteria"],
                status=proto["status"],
            )
            db.add(p)

            db.add(AuditLog(
                id=_uuid(),
                entity_type="protocol",
                entity_id=pid,
                action="create",
                performed_by=proto["created_by"],
                metadata_={
                    "title": proto["title"],
                    "role": proto["created_by"],
                    "phase": proto["phase"],
                },
            ))

            ver = ProtocolVersion(
                id=_uuid(),
                protocol_id=pid,
                version_number=proto["version_number"],
                content=proto["content"],
                comment=proto["comment"],
                compliance_score=proto["compliance_score"],
                generated_by="InHouse/Qwen3.5-122B",
            )
            db.add(ver)

            db.add(AuditLog(
                id=_uuid(),
                entity_type="protocol",
                entity_id=pid,
                action="ai_generate",
                performed_by="employee",
                metadata_={
                    "model": "InHouse/Qwen3.5-122B",
                    "sections": list(proto["content"].keys()),
                    "version": proto["version_number"],
                    "duration_ms": 4100 + added * 150,
                    "role": "employee",
                },
            ))

            if proto["compliance_score"]:
                db.add(AuditLog(
                    id=_uuid(),
                    entity_type="protocol",
                    entity_id=pid,
                    action="consistency_check",
                    performed_by="auditor",
                    metadata_={
                        "compliance_score": proto["compliance_score"],
                        "rf_compliance_score": proto["compliance_score"] - 4,
                        "role": "auditor",
                    },
                ))

            print(f"  [+] {proto['drug_name']} Phase {proto['phase']} | {proto['status']} | score={proto['compliance_score']}")
            added += 1
            existing_drugs.add(proto["drug_name"])

        await db.commit()
        print(f"\n  ✅ Добавлено: {added}, пропущено (дубли): {skipped}")

    await engine.dispose()


if __name__ == "__main__":
    asyncio.run(main())
